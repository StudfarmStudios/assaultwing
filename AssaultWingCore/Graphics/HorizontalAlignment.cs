namespace AW2.Graphics
{
    /// <summary>
    /// Type of horizontal alignment.
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// Aligned to the left.
        /// </summary>
        Left,

        /// <summary>
        /// Horizontally centered.
        /// </summary>
        Center,

        /// <summary>
        /// Aligned to the right.
        /// </summary>
        Right,

        /// <summary>
        /// Covers all available horizontal space.
        /// </summary>
        Stretch,
    }
}
