namespace AW2.Graphics
{
    /// <summary>
    /// Type of vertical alignment.
    /// </summary>
    public enum VerticalAlignment
    {
        /// <summary>
        /// Aligned to the top.
        /// </summary>
        Top,

        /// <summary>
        /// Vertically centered.
        /// </summary>
        Center,

        /// <summary>
        /// Aligned to the bottom.
        /// </summary>
        Bottom,

        /// <summary>
        /// Covers all available vertical space.
        /// </summary>
        Stretch,
    }
}
